# Logical Operators
a= True
b = False

#Looogical AND
and_operation = a and b # a*b -- True * False -- 1 * 0 -- 0 - False

#Logical OR
or_operation = a or b # True + False -- True -- 1

# Logical NOT
not_operation = not a # False

print("a AND b:", and_operation)
print("a OR b:", or_operation)
print("NOT a:", not_operation)